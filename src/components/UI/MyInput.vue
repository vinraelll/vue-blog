<template>
  <input 
    :value="modelValue" 
    @input="updateInput" 
    class="input" 
    type="text"
   >
</template>

<script>
export default {
  props: {
    modelValue: [String, Number]
  },
  name: 'my-input',
  methods: {
    updateInput(event) {
      this.$emit('update:modelValue', event.target.value)
    }
  }
}
</script>

<style scoped>
.input {
  width: 100%;
  margin-top: 15px;
  padding: 10px;
  border: 1px solid teal;
}
</style>